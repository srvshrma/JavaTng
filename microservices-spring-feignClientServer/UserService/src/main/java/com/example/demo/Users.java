package com.example.demo;

public class Users {
	
	private int id;
	private String name;
	
	public Users() {
		super();
	}

	public Users(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
